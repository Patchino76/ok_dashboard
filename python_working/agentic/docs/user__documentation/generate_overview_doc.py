doc = Document()

# --- Стилове ---
style = doc.styles["Normal"]
style.font.name = "Calibri"
style.font.size = Pt(11)


def H1(text):
    p = doc.add_heading(text, level=1)
    for run in p.runs:
        run.font.color.rgb = RGBColor(0x1F, 0x4E, 0x79)


def H2(text):
    p = doc.add_heading(text, level=2)
    for run in p.runs:
        run.font.color.rgb = RGBColor(0x2E, 0x74, 0xB5)


def P(text, bold=False, italic=False):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.bold = bold
    r.italic = italic
    return p


def BULLET(text):
    doc.add_paragraph(text, style="List Bullet")


def CAPTION(text):
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cr = cap.add_run(text)
    cr.italic = True
    cr.font.size = Pt(9)
    cr.font.color.rgb = RGBColor(0x66, 0x66, 0x66)


def UI_PLACEHOLDER(caption: str):
    """Маркер къде потребителят да добави UI скрийншот."""
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(f"[ МЯСТО ЗА UI СНИМКА: {caption} ]")
    r.italic = True
    r.font.color.rgb = RGBColor(0x99, 0x99, 0x99)
    r.font.size = Pt(10)
    CAPTION(f"Фигура: {caption}")


def IMAGE(rel_path: str, caption: str, width_inches: float = 5.5):
    """Вгражда реална графика от output/ папката."""
    img_path = AGENTIC_OUTPUT / rel_path
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if img_path.exists():
        run = p.add_run()
        run.add_picture(str(img_path), width=Inches(width_inches))
        CAPTION(f"Фигура: {caption}")
    else:
        r = p.add_run(f"[ ЛИПСВА ФАЙЛ: {rel_path} ]")
        r.italic = True
        r.font.color.rgb = RGBColor(0xCC, 0x00, 0x00)
        CAPTION(f"Фигура: {caption}")


